public class Computador {
    private String marca;
    private int cantidadMemoria;
    private String caracteristicasProcesaror;
    private String sistemaOperativo;
    private double precio;

    public Computador(String marca, int cantidadMemoria, String caracteristicasProcesaror, double precio) {
        this.marca = marca;
        this.cantidadMemoria = cantidadMemoria;
        this.caracteristicasProcesaror = caracteristicasProcesaror;
        this.sistemaOperativo = sistemaOperativo;
        this.precio = precio;
    }
}
